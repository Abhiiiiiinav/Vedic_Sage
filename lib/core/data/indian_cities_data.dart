  static List<CityLocation> get sortedCities {
    final sorted = List<CityLocation>.from(cities);
    sorted.sort((a, b) => a.name.compareTo(b.name));
    return sorted;
  }

  /// Get all unique states
  static List<String> get states {
    final stateSet = cities.map((c) => c.state).toSet().toList();
    stateSet.sort();
    return stateSet;
  }

  /// Get cities for a specific state
  static List<CityLocation> citiesForState(String state) {
    return cities.where((c) => c.state == state).toList()
      ..sort((a, b) => a.name.compareTo(b.name));
  }

  static CityLocation? findCity(String cityName) {
    final normalized = cityName.toLowerCase().trim();
    try {
      return cities.firstWhere(
        (city) => city.name.toLowerCase() == normalized,
      );
    } catch (_) {
      return cities.firstWhere((c) => c.name == 'Delhi'); // Default
    }
  }

  /// Search cities by partial name match
  static List<CityLocation> searchCities(String query) {
    if (query.isEmpty) return sortedCities;
    final q = query.toLowerCase().trim();
    return cities
        .where((c) =>
            c.name.toLowerCase().contains(q) ||
            c.state.toLowerCase().contains(q))
        .toList()
      ..sort((a, b) => a.name.compareTo(b.name));
  }
